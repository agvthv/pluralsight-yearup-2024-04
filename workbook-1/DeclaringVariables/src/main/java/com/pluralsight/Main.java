package com.pluralsight;

public class Main
{
    public static void main(String[] args)
    {
        // declare variables for each of the following:

        // a vehicle identification number in the range 1000000 - 9999999
            int VIN = 8893884;
            System.out.println("VIN: " + VIN);

        // a vehicle make /model (i.e. Ford Explorer)
            String carMakeModel = "Ford Explorer";
            System.out.println("Car Make/Model: " + carMakeModel);

        // a vehicle color
            String carColor = "White";
            System.out.println("Car Color: " + carColor);

        // whether the vehicle has a towing package
            boolean carTowPack = true;
            System.out.println("Tow Pack Included: " + carTowPack);

        // an odometer reading
            float carOdometer = 90876;
            System.out.println("Odometer: " + carOdometer);

        // a price
            double carPrice = 78098.99;
            System.out.println("Car Price: " + carPrice);

        // a quality rating (A, B, or C)
            char carQualityRating = 'A';
            System.out.println("Quality Rating: " + carQualityRating);

        // a phone number
            String phoneNumber = "(456)675-8976";
            System.out.println("Phone Number: " + phoneNumber);

        // a social security number
            String ssnNumber = "567-99-8756";
            System.out.println("SSN: " + ssnNumber);

        // a zip code
            int zipCode = 87654;
            System.out.println("Zip Code: " + zipCode);


    }
}